# in this code we will learn about escape sequences in Python strings

# /n is used to insert a new line in the string
str = "Hello\nWorld"
print("String with New Line Escape Sequence:")
print(str)

# /t is used to insert a tab space in the string for example:    (tab space)
str_tab = "Hello\tWorld"
print("String with Tab Escape Sequence:")
print(str_tab)

# \\ is used to insert a backslash in the string for example: \
str_backslash = "This is a backslash: \\"
print("String with Backslash Escape Sequence:")
print(str_backslash)

