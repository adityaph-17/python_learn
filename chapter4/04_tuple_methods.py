# demonstrating tuple methods

# Creating a tuple
a = (1, 2, 3, 4, 5)
print("Original Tuple:", a)


no = a.count(3) # counts occurrences of 3 in the tuple
print("Count of 3 in tuple a:", no)

index = a.index(4) # finds the index of first occurrence of 4
print("Index of 4 in tuple a:", index)

len = len(a) # gets the length of the tuple
print("Length of tuple a:", len)

