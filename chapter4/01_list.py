# in python list is an ordered collection of items which can be of different types
# lists are mutable i.e we can change the elements of the list

list = ["aditya", "asd", "xyz", "123", 123, 12.34, True]
print ("Original List:", list)

# accessing elements in a list using indexing
first_element = list[0] # accessing first element
print("First Element:", first_element)

#we can change the elements of the list
list[1] = "changed_value" # we are changing the second element of the list ,list is mutable
print("\n List after changing second element:", list)




