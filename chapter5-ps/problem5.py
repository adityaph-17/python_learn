# What is the type of 's'?

s = {}

print(type(s))  # Output: <class 'dict'>
# Explanation:
# In Python, curly braces {} are used to define a dictionary.
# An empty dictionary is created when we use {} without any key-value pairs.
# Therefore, the type of 's' is <class 'dict'>, which indicates that
# 's' is a dictionary.