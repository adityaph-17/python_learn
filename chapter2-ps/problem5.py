# this program finds the average of two numbers entered by the user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
average = (num1 + num2) / 2
print("The average of", num1, "and", num2, "is:", average)
# This code takes two numbers as input from the user, calculates their average, and prints the result.