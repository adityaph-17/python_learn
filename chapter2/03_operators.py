# arrithemetic operators
a = 10
b = 3
print("Addition:", a + b)          # Addition
print("Subtraction:", a - b)       # Subtraction
print("Multiplication:", a * b)    # Multiplication
print("Division:", a / b)          # Division
print("Floor Division:", a // b)   # Floor Division
print("Modulus:", a % b)           # Modulus
print("Exponentiation:", a ** b)   # Exponentiation ^ 

# comparison operators
x = 5
y = 10
print("Equal:", x == y)            # Equal
print("Not Equal:", x != y)        # Not Equal
print("Greater Than:", x > y)      # Greater Than
print("Less Than:", x < y)         # Less Than
print("Greater Than or Equal:", x >= y)  # Greater Than or Equal
print("Less Than or Equal:", x <= y)     # Less Than or Equal

# logical operators
p = True
q = False
print("Logical AND:", p and q)     # Logical AND if both p and q are True, returns True
print("Logical OR:", p or q)       # Logical OR if either p or q is True, returns True
print("Logical NOT:", not p)       # Logical NOT # if p is True, returns False
# This code demonstrates arithmetic, comparison, and logical operators in Python.

# Arithmetic Operators: +, -, *, /, //, %, **
# Comparison Operators: ==, !=, >, <, >=, <=
# Logical Operators: and, or, not

# Differences between operators:
# Arithmetic operators perform mathematical calculations on numerical values.
# Comparison operators compare two values and return a boolean result (True or False).
# Logical operators are used to combine multiple boolean expressions and return a boolean result.

# increment and decrement
num = 5
num += 1  # Increment by 1
print("Incremented Value:", num)

num = 5
num -= 1  # Decrement by 1
print("Decremented Value:", num)
# This code demonstrates increment and decrement operations using += and -= operators.
